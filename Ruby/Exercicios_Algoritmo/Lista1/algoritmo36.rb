# Ler dois números inteiros e imprimir a soma. Antes do resultado, deverá aparecer a mensagem: Soma 


puts "Informe um número: "
num1 = gets.chomp.to_i

puts "Informe um segundo número: "
num2 = gets.chomp.to_i

soma = num1 + num2 

puts "Soma:"
puts "#{num1} + #{num2} = #{soma}"



